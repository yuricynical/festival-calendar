        <h1>Rahugan Festival</h1>
        <img src="../../assets/assets/images/Rahugan.jpg" alt="Rahugan Festival" style="max-width: 100%; height: auto;">
        <p>
            The Rahugan Festival is an annual cultural and religious celebration in Basud, Camarines Norte, typically held from October 18 to 24. It coincides with the feast of St. Raphael the Archangel, the town's patron saint.
            The festival’s name comes from the term rahug, which refers to a cluster of coconuts, symbolizing strength through unity—an essential value among the Basudeños. The event honors the town's agricultural heritage, particularly its rich coconut production, while promoting community solidarity and cultural pride.
        </p>
        <p>
            The festival began in 2008 and has since grown into a week-long celebration featuring vibrant street dances, cultural performances, agro-tourism fairs, and local competitions like the Laro ng Lahi.
            Key highlights include the Grand Rahugan Parade, beauty pageants such as Mutya ng Rahugan, and unique events like "Gabi ng mga Lolo at Lola," which celebrates elders. The festival also features religious activities, including a fluvial procession along the Basud River​.
        </p>
        <p>
            Rooted in Basud’s history and identity, the Rahugan Festival showcases the town's close-knit community and agricultural significance.
            It serves as a reminder of the Basudeños’ spirit of cooperation and resilience, drawing visitors from across the region to experience the lively celebrations and partake in local traditions​.
        </p>
