        <h1>Pinyasan Festival</h1>
        <img src="../assets/images/Pinyasan.jpg" alt="Pinyasan Festival" style="max-width: 100%; height: auto;">
        <p>
            The Pinyasan Festival in Daet, Camarines Norte, is a vibrant annual celebration held every June 15-24 to showcase the town’s pride in its unique and sweet Formosa pineapples, often called the “Queen Pineapple of the Philippines.” The festival coincides with the feast day of Saint John the Baptist, the town’s patron saint, blending cultural, religious, and agricultural elements.
            It features lively events such as street dancing, a float parade, a beauty pageant, pineapple-themed contests, and a unique tradition of giving away free pineapples and pineapple-based treats like ice cream​.
        </p>
        <p>
            This festival originated in 1993 through the efforts of then-town councilor Tito S. Sarion, who sought to celebrate the Formosa pineapple's importance to local livelihoods. The name "Pinyasan," introduced in 2003, adds a distinct local touch.
            The celebration also honors the indigenous Kabihug tribe, who are among the pineapple farmers. The region's unique soil composition and farming techniques contribute to the fruit's exceptional sweetness​.
        </p>
        <p>
            Over the years, the Pinyasan Festival has grown from a simple town event into a provincial attraction, significantly boosting tourism.
            Highlights include a Guinness World Record attempt for the longest pineapple chain and other community-centered activities like art exhibits, fun runs, and beach events at Bagasbas Beach. The festival not only celebrates the pineapple industry but also showcases the culture, creativity, and hospitality of Daet​.
        </p>
